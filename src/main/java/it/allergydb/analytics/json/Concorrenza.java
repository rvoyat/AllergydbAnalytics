package it.allergydb.analytics.json;

public class Concorrenza {
    
    private String allergene;
    private Integer numeroPersone;
    /**
     * @return the allergene
     */
    public String getAllergene() {
        return allergene;
    }
    /**
     * @param allergene the allergene to set
     */
    public void setAllergene(String allergene) {
        this.allergene = allergene;
    }
    /**
     * @return the numeroPersone
     */
    public Integer getNumeroPersone() {
        return numeroPersone;
    }
    /**
     * @param numeroPersone the numeroPersone to set
     */
    public void setNumeroPersone(Integer numeroPersone) {
        this.numeroPersone = numeroPersone;
    }
    
    

}
