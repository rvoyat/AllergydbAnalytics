package it.allergydb.analytics.json;

public class ClinicCase {

    private Integer numAllergies;
    private Long numPeoples;
    /**
     * @return the numAllergies
     */
    public Integer getNumAllergies() {
        return numAllergies;
    }
    /**
     * @param numAllergies the numAllergies to set
     */
    public void setNumAllergies(Integer numAllergies) {
        this.numAllergies = numAllergies;
    }
    /**
     * @return the numPeoples
     */
    public Long getNumPeoples() {
        return numPeoples;
    }
    /**
     * @param numPeoples the numPeoples to set
     */
    public void setNumPeoples(Long numPeoples) {
        this.numPeoples = numPeoples;
    }
    
    
}
